//WAP to find greatest of three numbers using if -else ladder

public class GreatestNoIfelse {

	public static void main(String[] args) {
		int i=40;
        int j=60;
        int k=80;
		  
		  if (i >= j && i >= k)
			  System.out.println("greatst number is i ="+i);

		  else if (j >= i && j >= k)
			  System.out.println(" greatst number is j ="+j);

		  else
			  System.out.println(" greatst number is k = "+k);



	}

}
